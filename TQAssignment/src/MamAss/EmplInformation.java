package MamAss;

public class EmplInformation {
	
	private String Day;
	String Month;
	private int Year;
	
	public void set_Day(String day)
	{
		this.Day=day;
	}
	public String get_Day()
	{
		return Day;
	}
	public void set_Month(String month)
	{
		this.Month=month;
	}
	public String get_Month()
	{
		return Month;
	}
	public void set_Year(int year)
	{
		this.Year=year;
	}
	public int get_Year()
	{
		return Year;
	}

}
