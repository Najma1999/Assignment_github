package MamAss;

public class Employee {
	private String Name;
	private double Salary;
	
	public void set_Name(String name)
	{
		this.Name=name;
	}
	public String get_Name()
	{
		return Name;
	}
    public void set_Salary(double salary)
    {
    	this.Salary=salary;
    }
    public double get_Salary()
    {
    	return Salary;
    }
}
