package MamAss;

public class EmplDepartment {
	
	private String deptName;
	private int deptId;

	public void set_DeptName(String deptname)
	{
		this.deptName=deptname;
	}
	public String get_DeptName()
	{
		return deptName;
	}
	public void set_DeptId(int deptid)
	{
		this.deptId=deptid;
	}
	public int get_DeptId()
	{
		return deptId;
	}
}
